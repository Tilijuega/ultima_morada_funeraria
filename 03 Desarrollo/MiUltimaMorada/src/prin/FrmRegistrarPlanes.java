    void Agregar(){
        String cla = txtClave.getText();
        String tit = txtTitulo.getText();
        String cos = txtCosto.getText();
        String mes = txtMeses.getText();
        
        try{
            if (cla.equals("") || tit.equals("") || cos.equals("") ||mes.equals("")){
                JOptionPane.showMessageDialog(null, "Faltan ingresar datos");
              
            } else{
                String sql = "insert into plan_previsor(Titulo, Costo_total, Meses_Max, Clave) values ('"+tit+"','"+cos+"','"+mes+"','"+cla+"')";
            conet = con1.getConnection();
            st = conet.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Datos Agregados");
            
            }
            
        }catch (Exception e){
        }
    }

    
